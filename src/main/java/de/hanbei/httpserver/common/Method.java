package de.hanbei.httpserver.common;

/**
 * Enumeration of all supported HTTP methods.
 */
public enum Method {
    /**
     * HTTP GET Method.
     */
    GET,
    /**
     * HTTP POST Method.
     */
    POST,

    /**
     * HTTP PUT Method.
     */
    PUT,

    /**
     * HTTP DELETE Method.
     */
    DELETE

}
